from __future__ import annotations

from dataclasses import dataclass

@dataclass(frozen=True)
class ConstellationPreset:
    name: str
    points: tuple[tuple[float, float, float], ...]
    edges: tuple[tuple[int, int], ...]

def preset_names() -> list[str]:
    return list(CONSTELLATION_PRESETS.keys())

def get_preset(name: str) -> ConstellationPreset:
    return CONSTELLATION_PRESETS[name]

def build_svg(name: str, width: int = 640, height: int = 360) -> str:
    preset = get_preset(name)
    parts = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}" fill="none">',
        '<rect width="100%" height="100%" fill="none"/>',
    ]
    for start, end in preset.edges:
        x1, y1, _ = preset.points[start]
        x2, y2, _ = preset.points[end]
        parts.append(f'<line x1="{x1 * width:.2f}" y1="{y1 * height:.2f}" x2="{x2 * width:.2f}" y2="{y2 * height:.2f}" stroke="rgba(172,188,212,0.22)" stroke-width="1"/>')
    for x, y, r in preset.points:
        radius = max(0.9, r)
        parts.append(f'<circle cx="{x * width:.2f}" cy="{y * height:.2f}" r="{radius:.2f}" fill="rgba(236,242,251,0.86)"/>')
    parts.append('</svg>')
    return "".join(parts)

